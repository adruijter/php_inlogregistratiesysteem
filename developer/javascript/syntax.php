<h4>Operators</h4>
<script>
    var x = 8, y = 4;
    
    document.write(x + " + " + y + " = " + (x + y) + "<br>");
    document.write(x + " - " + y + " = " + (x - y) + "<br>");
    document.write(x + " * " + y + " = " + (x * y) + "<br>");
    document.write(x + " / " + y + " = " + (x / y) + "<br>");
    document.write(x + " % " + y + " = " + (x % y));
</script>
